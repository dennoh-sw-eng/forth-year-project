    public void joinBranchRecordsToTable(){
    
        try{
            Connection con = DBConnection.getConnection();
            java.sql.Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT drugId, drugName, category, drugQuantity, 'cbd' AS branch FROM cbd " +
                "UNION " +
                "SELECT drugId, drugName, category, drugQuantity, 'kilimani' AS branch FROM kilimani " +
                "UNION " +
                "SELECT drugId, drugName, category, drugQuantity, 'karen' AS branch FROM karen " +
                "UNION " +
                "SELECT drugId, drugName, category, drugQuantity, 'meru' AS branch FROM meru " +
                "UNION " +
                "SELECT drugId, drugName, category, drugQuantity, 'chuka' AS branch FROM chuka");
            
            while (rs.next()){
                String drugIds = rs.getString("drugId");
                String names = rs.getString("drugName");
                String branch = rs.getString("branch");
                String categs = rs.getString("category");
                String quantitys = rs.getString("drugQuantity");
                
                Object[] obj = {drugIds, names, branch, categs, quantitys};
            model = (DefaultTableModel)branchRecordTable.getModel();
            model.addRow(obj);
                
            }
            
        }catch(Exception e){
            e.printStackTrace();
        }
}
