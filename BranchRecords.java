    public void searchMethod(){
        String branchName = txt_branchSearch.getText();
        String drugName = txt_drugSearch.getText();
        String category = txt_drugSearch.getText();
        
        try{
            Connection con = DBConnection.getConnection();
            String sql = "SELECT * FROM " + branchName + " WHERE drugName = ? OR category = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            
            pst.setString(1, drugName);
            pst.setString(2, category);
            
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                String drugIds = rs.getString("drugId");
                String drugNames = rs.getString("drugName");
                String branchNames = rs.getString("branchName");
                String categorys = rs.getString("category");
                String quantitys = rs.getString("drugQuantity");
                
                Object[] obj = {drugIds, drugNames, branchNames, categorys, quantitys};
            model = (DefaultTableModel) branchRecordTable.getModel();
            model.addRow(obj);           
            }
                     
        }catch (Exception e){
            e.printStackTrace();
    }
        
    }
